package com.zxu.picturesxiangce.bean;

public class Image {
    private String time_image;
    private String url_image;
    private String user_name;
    private String target_video;

    public String getTime_image() {
        return time_image;
    }

    public void setTime_image(String time_image) {
        this.time_image = time_image;
    }

    public String getUrl_image() {
        return url_image;
    }

    public void setUrl_image(String url_image) {
        this.url_image = url_image;
    }

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getTarget_video() {
        return target_video;
    }

    public void setTarget_video(String target_video) {
        this.target_video = target_video;
    }
}

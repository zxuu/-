package com.zxu.picturesxiangce.bean;

public class Comment {
    private String id;
    private String content;
    private String target_video;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getTarget_video() {
        return target_video;
    }

    public void setTarget_video(String target_video) {
        this.target_video = target_video;
    }
}
